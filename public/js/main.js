(() => {
  // <stdin>
  console.log("This site was generated by Hugo.");
  var navButton = document.getElementById("hamburgerMenu");
  var verticalNav = document.getElementById("verticalNav");
  var horizontalNav = document.getElementById("horizontalNav");
  var hListLines = horizontalNav.querySelectorAll("li");
  var vListItems = verticalNav.querySelectorAll("li a");
  var hListItems = horizontalNav.querySelectorAll("li a");
  var toggleNavigation = () => {
    verticalNav.classList.toggle("hideVerticalNav");
    verticalNav.classList.toggle("showVerticalNav");
  };
  var closeNavigation = () => {
    navButton.setAttribute("aria-expanded", "false");
    toggleNavigation();
  };
  var handleFocus = (e) => {
    if (e.currentTarget.parentElement) {
      e.currentTarget.parentElement.classList.add("verticalNavListFocused");
    }
  };
  var handleBlur = (e) => {
    if (e.currentTarget.parentElement) {
      e.currentTarget.parentElement.classList.remove("verticalNavListFocused");
    }
  };
  var handleMenuExit = (e) => {
    const navList = e.currentTarget.closest(".verticalNavList");
    if (!e.relatedTarget || !navList.contains(e.relatedTarget)) {
      closeNavigation();
    }
  };
  navButton.addEventListener("click", toggleNavigation);
  vListItems[vListItems.length - 1].addEventListener("blur", handleMenuExit);
  vListItems.forEach((item) => {
    item.addEventListener("focus", handleFocus);
    item.addEventListener("blur", handleBlur);
  });
  hListItems.forEach((item) => {
    item.addEventListener("focus", handleFocus);
    item.addEventListener("blur", handleBlur);
  });
  hListLines.forEach((item) => {
    item.addEventListener("mouseover", (e) => e.currentTarget.firstElementChild.classList.add("nav-link-hovered"));
    item.addEventListener("mouseleave", (e) => e.currentTarget.firstElementChild.classList.remove("nav-link-hovered"));
  });
  window.addEventListener("keyup", (e) => {
    if (e.key === "Escape") {
      navButton.focus();
      closeNavigation();
    }
  });
})();
